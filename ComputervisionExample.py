class ConvNet(nn.Module):       #Model definition
    def __init__(self):
        super(ConvNet, self).__init__()         #Take standard methods from the standard Neural Network class
        self.layer1 = nn.Sequential(            #Construct a new layer with a convolutional layer, a ReLU and a maxpooling layer
            nn.Conv2d(3, 32, kernel_size=5, stride=1, padding=2),   #One input channel (image), 32 output channels (filters applied), 5x5 kernel size, 1 stride (shift between windows), 2 padding (check formula for padding)
            nn.ReLU(),      #ReLU layer to make data go through non-linear function
            nn.MaxPool2d(kernel_size=2, stride=2))  #Maxpooling: take max() of the values in the marked kernel, kernel size= the dimensions of kernel, stride/shifts (similar)
        #Kernel size can have more complex structure by defining a tuple of (x,y)
        self.layer2 = nn.Sequential(        #second layer
            nn.Conv2d(32, 64, kernel_size=5, stride=1, padding=2),
            nn.ReLU(),
            nn.MaxPool2d(kernel_size=2, stride=2))
        self.drop_out = nn.Dropout()    #randomly zeroing some of the elements in the input tensor
        self.fc1 = nn.Linear(7 * 7 * 64, 1000)  #creating a fully connected layer, arguments are number of nodes and number of nodes in next layer
        self.fc2 = nn.Linear(1000, 10)          #another fully connected layer to reach output layer

    def forward(self, x):
        out = self.layer1(x)            #pass data from input to first layer
        out = self.layer2(out)          #first layer -> second layer
        out = out.reshape(out.size(0), -1)  #Reshape data from 7x7x64 to 3164x1 (flatten out)
        out = self.drop_out(out)
        out = self.fc1(out)
        out = self.fc2(out)
        return out



def imshow(inp, title=None):
    """Imshow for Tensor."""
    inp = inp.numpy().transpose((1, 2, 0))
    mean = np.array([0.485, 0.456, 0.406])
    std = np.array([0.229, 0.224, 0.225])
    inp = std * inp + mean
    inp = np.clip(inp, 0, 1)
    plt.imshow(inp)
    if title is not None:
        plt.title(title)
    plt.pause(10.0)  # pause a bit so that plots are updated




def train_model(model, criterion, optimizer, scheduler, num_epochs=25):
    since = time.time()

    best_model_wts = copy.deepcopy(model.state_dict())
    best_acc = 0.0

    for epoch in range(num_epochs):
        print('Epoch {}/{}'.format(epoch, num_epochs - 1))
        print('-' * 10)

        # Each epoch has a training and validation phase
        for phase in ['train', 'val']:
            if phase == 'train':
                model.train()  # Set model to training mode
            else:
                model.eval()   # Set model to evaluate mode

            running_loss = 0.0
            running_corrects = 0

            # Iterate over data.
            for inputs, labels in dataloaders[phase]:
                inputs = inputs.to(device)
                labels = labels.to(device)

                # zero the parameter gradients
                optimizer.zero_grad()

                # forward
                # track history if only in train
                with torch.set_grad_enabled(phase == 'train'):
                    outputs = model(inputs)
                    _, preds = torch.max(outputs, 1)
                    loss = criterion(outputs, labels)

                    # backward + optimize only if in training phase
                    if phase == 'train':
                        loss.backward()
                        optimizer.step()

                # statistics
                running_loss += loss.item() * inputs.size(0)
                running_corrects += torch.sum(preds == labels.data)
            if phase == 'train':
                scheduler.step()

            epoch_loss = running_loss / dataset_sizes[phase]
            epoch_acc = running_corrects.double() / dataset_sizes[phase]

            print('{} Loss: {:.4f} Acc: {:.4f}'.format(
                phase, epoch_loss, epoch_acc))

            # deep copy the model
            if phase == 'val' and epoch_acc > best_acc:
                best_acc = epoch_acc
                best_model_wts = copy.deepcopy(model.state_dict())

        print()

    time_elapsed = time.time() - since
    print('Training complete in {:.0f}m {:.0f}s'.format(
        time_elapsed // 60, time_elapsed % 60))
    print('Best val Acc: {:4f}'.format(best_acc))

    # load best model weights
    model.load_state_dict(best_model_wts)
    return model

def visualize_model(model, num_images=6):
    was_training = model.training
    model.eval()
    images_so_far = 0
    fig = plt.figure()

    with torch.no_grad():
        for i, (inputs, labels) in enumerate(dataloaders['val']):
            inputs = inputs.to(device)
            labels = labels.to(device)

            outputs = model(inputs)
            _, preds = torch.max(outputs, 1)

            for j in range(inputs.size()[0]):
                images_so_far += 1
                ax = plt.subplot(num_images//2, 2, images_so_far)
                ax.axis('off')
                ax.set_title('predicted: {}'.format(class_names[preds[j]]))
                imshow(inputs.cpu().data[j])

                if images_so_far == num_images:
                    model.train(mode=was_training)
                    return
        model.train(mode=was_training)

